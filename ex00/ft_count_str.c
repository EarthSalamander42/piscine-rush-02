int		ft_count_str(char *str)
{
	int a;
	int nb;
	int word;

	a = 0;
	nb = 0;
	word = 0;
	while (str[a] != '\0')
	{
		if (str[a] == '\n')
			word = 0;
		if (!(str[a] == '\n') && word == 0)
		{
			word = 1;
			nb++;
		}
		a++;
	}
	return (nb);
}
